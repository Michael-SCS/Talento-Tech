#crear dos arrays
import numpy as np

a = np.array([1, 2, 3, 4, 5])
b = np.array([6, 7, 8, 9, 10])

#suma, resta, multiplicación y división de los arrays
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b

#f: permite la interpolación de cadenas.
print(f"La suma es {suma}")
print(f"La resta es {resta}")
print(f"La multiplicación es {multiplicacion}")
print(f"La división es {division}")
