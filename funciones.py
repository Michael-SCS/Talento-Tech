#Crear una función que enliste números y me devuelva la lista ordenada de manera ascendente

lista = [5, 3, 8, 1, 9, 2, 6, 4, 7]

def ordenarLista(lista):
    lista.sort()
    return lista
print(ordenarLista(lista))
