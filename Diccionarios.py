#¿Cómo crear un diccionario?
diccionario = {
    "cedula" : 123456789,
    "nombre" : "Juan",
    "apellido" : "Perez",
    "edad" : 25,
    "casado" : False
}
print(diccionario)
#¿Cómo agregar un elemento a un diccionario?
diccionario["Profesión"] = "Médico"
print(diccionario)

#modificar el valor de una llave
diccionario["edad"] = 26
print(diccionario)